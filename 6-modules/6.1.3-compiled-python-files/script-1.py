# "Compiled" Python files
# Para aumentar a velocidade do carregamento dos módulos, Python faz cache com
# a versão compilada de cada módulo na pasta "__pycache__"

# Dentro dessa pasta, podemos encontrar o arquivo compilado
# no padrão "__pycache__/<module>.<version>.pyc"

# Python verifica se houve modificação no código de origem dos módulos